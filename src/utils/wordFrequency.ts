/** Strip diacritics for stop-word comparison while preserving display form. */
function normalize(word: string): string {
  return word.normalize("NFD").replace(/[\u0300-\u036f]/g, "");
}

export function getWordFrequencies(
  texts: string[],
  lang: "es" | "en"
): Array<{ text: string; value: number }> {
  const stopWords = lang === "es" ? STOP_WORDS_ES : STOP_WORDS_EN;
  const counts = new Map<string, number>();
  // Track first-seen accented form as the display form for each normalized key
  const displayForm = new Map<string, string>();

  for (const text of texts) {
    const rawWords = text
      .toLowerCase()
      .replace(/[^\p{L}\s]/gu, " ")
      .split(/\s+/);

    for (const word of rawWords) {
      if (word.length <= 2) continue;
      const key = normalize(word);
      if (key.length <= 2 || stopWords.has(key)) continue;
      if (!displayForm.has(key)) displayForm.set(key, word);
      counts.set(key, (counts.get(key) ?? 0) + 1);
    }
  }

  return Array.from(counts.entries())
    .map(([key, value]) => ({ text: displayForm.get(key) ?? key, value }))
    .sort((a, b) => b.value - a.value)
    .slice(0, 40);
}
